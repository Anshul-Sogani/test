import java.util.Scanner;

class t_for{
	
	public static void main(String a[]){



		for(int i= 0 ; i<=3 ; i++){

			for(int j=4 ; j>i  ;j--){

				if(j % 2 == 0){
					System.out.print(0);
				}else{

				System.out.print( j );
				}
			}

			System.out.println("");
		}



		for(int i = 4 ; i>0; i--){

			for(int j=i ; j>0 ; j--){



				if(j % 2 == 0){
					System.out.print(0);
				}else{

				System.out.print( j );
				}
			}
				System.out.println("");
		}





	}
}


		

			

				
